package com.saray.sinotk.entity;

public class Quality {
	
	public String stationId;
	public String quality;
	public String qualityItem;
	public String qualityIndex;
	
		
	public String getStationId() {
		return stationId;
	}
	public void setStationId(String stationId) {
		this.stationId = stationId;
	}
	public String getQuality() {
		return quality;
	}
	public void setQuality(String quality) {
		this.quality = quality;
	}
	public String getQualityItem() {
		return qualityItem;
	}
	public void setQualityItem(String qualityItem) {
		this.qualityItem = qualityItem;
	}
	public String getQualityIndex() {
		return qualityIndex;
	}
	public void setQualityIndex(String qualityIndex) {
		this.qualityIndex = qualityIndex;
	}
	
	
}
